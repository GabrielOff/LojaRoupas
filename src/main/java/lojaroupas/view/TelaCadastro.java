    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String cor = txtCorcadastrar.getText();
        String tamanho = txtTamanhocadastrar.getText();
        String precoTexto = txtPrecocadastrar.getText();
        String marca = txtMarcaCadastrar.getText();

        if (cor.isEmpty() || tamanho.isEmpty() || precoTexto.isEmpty() || marca.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                double preco = Double.parseDouble(precoTexto);
                CamisasDAO.adcionarCamisas(new Camisas(cor, tamanho, preco, marca));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Formato de pre√ßo inv√°lido", "Erro", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

