    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            String marca = txtConsultarmarca.getText();

            if (!marca.isEmpty()) {
                List<Camisas> camisasLista = new ArrayList<>();

                try {
                    camisasLista = CamisasDAO.listagemCamisas(marca);

                    if (!camisasLista.isEmpty()) {
                        DefaultTableModel model = new DefaultTableModel(new Object[]{"ID", "Marca", "Cor", "Tamanho", "PreÃ§o", "Fabricante"}, 0);
                        camisasLista.forEach(c -> {
                            Object[] row = new Object[]{c.getIdCamisa(), c.getMarca(), c.getCor(), c.getTamanho(), c.getPreco(), c.getFabricante()};
                            model.addRow(row);
                        });

                        TabelaConsultar.setModel(model);
                    } else {
                        JOptionPane.showMessageDialog(this, "Nenhum registro encontrado");
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Digite a Marca!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao consultar as camisas");
        }

    }//GEN-LAST:event_btnConsultarActionPerformed

