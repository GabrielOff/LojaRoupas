    private void btnEditarconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarconsultarActionPerformed
        int rowIndex = TabelaConsultar.getSelectedRow();

        if (rowIndex != -1) {
            Object[] rowData = new Object[TabelaConsultar.getColumnCount()];

            for (int i = 0; i < TabelaConsultar.getColumnCount(); i++) {
                rowData[i] = TabelaConsultar.getValueAt(rowIndex, i);
            }

            TelaEditarItem telaEditarItem = new TelaEditarItem(rowData);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para editar.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnEditarconsultarActionPerformed

