    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int row = TabelaConsultar.getSelectedRow();

        if (row >= 0) {
            int option = JOptionPane.showConfirmDialog(this, "Excluir Camisa?", "ConfirmaÃ§Ã£o de exclusÃ£o", JOptionPane.YES_NO_OPTION);

            if (option == JOptionPane.YES_OPTION) {
                try {
                    int id = (int) TabelaConsultar.getValueAt(row, 0);
                    boolean erroExclusao = CamisasDAO.excluirCamisas(id);

                    if (!erroExclusao) {
                        DefaultTableModel model = (DefaultTableModel) TabelaConsultar.getModel();
                        model.removeRow(row);
                        JOptionPane.showMessageDialog(this, "Camisa excluÃ­da com sucesso.");
                    }
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Erro ao excluir a camisa: " + ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma camisa para excluir.");
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

